    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String rut = tfdRutSocio.getText().trim();
        Socio socio = buscarSocioPorRut(rut);

        if (socio == null) {
            JOptionPane.showMessageDialog(this, "Socio no encontrado.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JOptionPane.showMessageDialog(this, "Socio encontrado: " + socio.getNombre(), "Éxito", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        String rut = tfdRutSocio.getText().trim();
        Socio socio = buscarSocioPorRut(rut);

        if (socio == null) {
            JOptionPane.showMessageDialog(this, "Debes buscar un socio válido antes de pagar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int numeroCuota = Integer.parseInt(tfdNumeroCuota.getText().trim());
            pagarCuota(socio, numeroCuota);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El número de cuota debe ser un valor numérico.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnPagarActionPerformed

