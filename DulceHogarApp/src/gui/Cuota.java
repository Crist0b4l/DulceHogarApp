     private void cargarDatosSocios() {
        socios = new ArrayList<>();

        // Crear datos de ejemplo
        Socio socio1 = new Socio("12345678-9", "Juan", "Perez", "Yoma", "987654321", "Avenida Siempre Viva", "Santiago", 1111);
        socio1.agregarCuota(new Cuota(1, 5000, false));
        socio1.agregarCuota(new Cuota(2, 5000, false));

        Socio socio2 = new Socio("25123456-7", "Maria", "Yoma", "Perez", "123456789", "Avenida A Veces Viva", "Santiago", 1112);
        socio2.agregarCuota(new Cuota(1, 7000, true)); // Cuota ya pagada
        socio2.agregarCuota(new Cuota(2, 7000, false));

        // Agregar a la lista
        socios.add(socio1);
        socios.add(socio2);
    }
     
     
     private Socio buscarSocioPorRut(String rut) {
        for (Socio socio : socios) {
            if (socio.getRut().equalsIgnoreCase(rut)) {
                return socio;
            }
        }
        return null;
    }

     
     private void pagarCuota(Socio socio, int numeroCuota) {
        Cuota cuota = socio.buscarCuota(numeroCuota);
        if (cuota == null) {
            JOptionPane.showMessageDialog(this, "La cuota no existe.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (cuota.isPagada()) {
            JOptionPane.showMessageDialog(this, "La cuota ya está pagada.", "Información", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        cuota.setPagada(true); // Marcar como pagada
        JOptionPane.showMessageDialog(this, "Cuota pagada exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
    }
