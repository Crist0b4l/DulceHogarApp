public class Cuota {
    private int numero;
    private double monto;
    private boolean pagada;

    public Cuota(int numero, double monto, boolean pagada) {
        this.numero = numero;
        this.monto = monto;
        this.pagada = pagada;
    }

    public int getNumero() {
        return numero;
    }

    public double getMonto() {
        return monto;
    }

    public boolean isPagada() {
        return pagada;
    }

    public void setPagada(boolean pagada) {
        this.pagada = pagada;
    }
}